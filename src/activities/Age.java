package activities;

public class Age {

	public static void main(String[] args) {
		int age=56;
		if(age<56) {
		System.out.println("person is child");
		}
		else if(age>56) {
			System.out.println("person is teenager");
			
		}
		else if(age<=56) {
			System.out.println("person is adult");
		}
		else {
			System.out.println("person is senior citizen");
		}
			

	}

}
